class Solution {
  public:
    // Function to find the first non-repeating character in a string.
    char nonRepeatingChar(string &s) {
        // Your code here
        unordered_map<int,int>mp;
        for(auto a:s)mp[a]++;
        for(auto a:s) {
            if(mp[a]==1) return a;
        }
        return '$';
    }
};


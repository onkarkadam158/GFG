class Solution {
  public:
    vector<int> removeDuplicate(vector<int>& arr) {
        // code here
        map<int,int>mp;
        vector<int>res;
        for(auto a:arr){
            if(mp.find(a)==mp.end()){
                res.push_back(a);
            }
            mp[a]++;
        }
        return res;
    }
};

